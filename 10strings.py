#print strings accoring to location
course='Python for Beginners'
print(course[1])
#count starts from 0
print(course[3])
print(course[-1])
#negative goes/starts from right most
print(course[0:3])
#[0:3] means start from including 0 upto less than 3
print(course[0:])
#start from including zero upto last
print(course[1:])
#start from including 1 upto last 
print(course[:3])
#start from including 0 and upto less than 3
print(course[:])
#start from 0 to last means all